package com.paragon.client.managers.social;

/**
 * @author Wolfsurge
 */
public enum Relationship {
    /**
     * Do not attack them
     */
    FRIEND,
    /**
     * Attack them, but do display them as neutral instead of enemy
     */
    NEUTRAL,
    /**
     * Attack them and display them as an enemy
     */
    ENEMY
}
